    public void alteraVisor(String tecla) {
        if ((resposta.getText().equals("0")) || (zerartela)) {
            resposta.setText(tecla);
            zerartela = false;
        } else {
            resposta.setText(resposta.getText() + tecla);
        }
    }

    public void Virga(String virgula1) {
        if (resposta.getText().equals(resposta.getText() + "")) {
            if (resposta.getText().equals("")) {
                resposta.setText("0" + virgula1);
            } else {
                resposta.setText(resposta.getText() + virgula1);
            }
            virgula.setEnabled(false);
        }
    }

