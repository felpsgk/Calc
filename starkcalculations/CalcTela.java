    private void porcentagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_porcentagemActionPerformed
        valor2 = Double.parseDouble(resposta.getText());
        virgula.setEnabled(true);
        if (operacao == "soma") {
            resultado = valor1 + (valor1 * (valor2 / 100));
            resposta.setText(Double.toString(resultado));
        }
        if (operacao == "menos") {
            resultado = valor1 - (valor1 * (valor2 / 100));
            resposta.setText(Double.toString(resultado));
        }
        if (operacao == "multiplicacao") {
            resultado = valor1 * (valor1 * (valor2 / 100));
            resposta.setText(Double.toString(resultado));
        }
        if (operacao == "divisao") {
            resultado = valor1 / (valor1 * (valor2 / 100));
            resposta.setText(Double.toString(resultado));
        }
    }//GEN-LAST:event_porcentagemActionPerformed

    private void invertsinalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_invertsinalActionPerformed
        valor1 = Double.parseDouble(resposta.getText());
        if (valor1 < 0) {
            resposta.setText(Double.toString(valor1 * (-1)));
        } else if (valor1 > 0) {
            resposta.setText("-" + valor1);
        }
    }//GEN-LAST:event_invertsinalActionPerformed

    private void backspaceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backspaceActionPerformed
        if (resposta.getText().substring(resposta.getText().length() - 1).equals(".")) {
            virgula.setEnabled(true);
        }
        if (resposta.getText().length() == 1) {
            resposta.setText("0");
        } else {
            resposta.setText(resposta.getText().substring(0, resposta.getText().length() - 1));
        }
    }//GEN-LAST:event_backspaceActionPerformed

